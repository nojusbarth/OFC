/**
 * Repräsentiert einen Zeitpunkt für die Hintergrundgestaltung der Simulation.
 */
export enum DayTime {
    /**
     * Repräsentiert eine Mittagszeit-Umgebung
     */
    NOON,
    /**,
     * Repräsentiert eine Sonnenuntergang-Umgebung
     */
    SUNSET,
    /**
     * Repräsentiert eine Nacht-Umgebung
     */
    NIGHT
}